public class SwitchDemo {

	public static void main(String[] args) {
		int month=4;
		switch (month) {
		case 1:
			System.out.println("春季");
			break;
		case 2:
			System.out.println("春季");
			break;
		case 3:
			System.out.println("春季");
			break;
		case 4:
			System.out.println("春季");
			break;
		case 5:
			System.out.println("夏季");
			break;

		default:
			System.out.println("没有月份，请检查输入");
			break;
		}
	
	}

}
